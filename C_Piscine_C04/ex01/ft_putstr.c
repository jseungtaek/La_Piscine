#include<unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	strlen;

	strlen = -1;
	while (str[++strlen] != '\0')
		ft_putchar(str[strlen]);
}
